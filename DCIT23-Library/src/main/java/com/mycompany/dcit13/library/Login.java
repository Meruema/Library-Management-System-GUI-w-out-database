    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Get input values
        String fullName = txtFullName.getText().trim();
        String studentNumStr = txtStudentNum.getText().trim();
        String course = (String) Course.getSelectedItem();

        // Validate input | Conditional Statement
        if (fullName.isEmpty() || studentNumStr.isEmpty() || course.isEmpty()) {
            // Show error if any field is empty
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else { // If all the fields have values
            // Check if fullName contains only alphabetic characters and spaces
            if (!fullName.matches("[a-zA-Z\\s]+")) {
                JOptionPane.showMessageDialog(this, "Full Name should only contain alphabetic characters.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int studentNum;
            try {
                studentNum = Integer.parseInt(studentNumStr); // Try to parse as integer
            } catch (NumberFormatException e) {
                // Show error if student number is not a valid integer
                JOptionPane.showMessageDialog(this, "Student number should be a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // instantiation
            MainFrameUser mainFrameUser = new MainFrameUser();
            mainFrameUser.setVisible(true);
            this.dispose(); // Close the current login frame
            
            if (fullName.equals("admin") && studentNumStr.equals("1234567890") && course.equals("BS Computer Science")) {
                MainFrameAdmin mainFrameAdmin = new MainFrameAdmin();
                mainFrameAdmin.setVisible(true);
                this.dispose(); // Close the current login frame
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

